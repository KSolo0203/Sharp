void Print2dArray(int[,] arr)
{
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
        {
            if (arr[i,j] == 1)
            {
                Console.Write($"##");
            }
            else if (arr[i,j] == 2)
            {
                Console.Write($".,");
            }
            else
                Console.Write("  ");
        }
        Console.WriteLine();
    }
}

void FloodPic(int row, int column)
{
    if (pic[row,column] == 0)
    {
        pic[row,column] = 2;
        FloodPic(row-1,column);
        FloodPic(row,column-1);
        FloodPic(row+1,column);
        FloodPic(row,column+1);
    }
}

